	--判断背包空闲空间是否足够
	local FreeSize = role.GetBagFreeSize(RoleID)
	if n ==1 then
		if(FreeSize < 4) then
			--提示玩家背包空间不足
			bRet = 40
		end
		return bRet, bIgnore
	else
		if(FreeSize < 1) then
			--提示玩家背包空间不足
			bRet = 40
		end
	return bRet, bIgnore
	end
end


