					WaBao_InsCre(MapID,InstanceID,a,b,c,d,e)
					end
				end
			end
			--刷新两个宝箱
			local m = map.MapCreateCreature(MapID, InstanceID, 3011063, 129, 1312, 439)
			local n = map.MapCreateCreature(MapID, InstanceID, 3011063, 67, 1350, 318)
			map.SetMapScriptData(MapID,InstanceID,1,7,m)
			map.SetMapScriptData(MapID,InstanceID,1,8,n)
			--对话后删除此NPC
			map.MapDeleteCreature(MapID, InstanceID, TargetID)
			TalkIndex = -1
		end
	else
			local MsgID = msg.BeginMsgEvent()
			msg.AddMsgEvent(MsgID, 20, 86)	-- "氏族头领已经出现，请将其击败。"
			msg.AddMsgEvent(MsgID, 24, TargetID)
			msg.DispatchRoleMsgEvent(RoleID, MsgID)
	end
end
